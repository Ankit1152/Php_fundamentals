<?php
$a = 10;
$b = 20;

// and / && operator
if($a == 10 && $b == 20) {
    echo "Both conditions are true";
}

// or / || operator

if($a == 10 || $b == 20) {
    echo "At least one condition is true";
}


// xor
if($a == 10 xor $b == 20) {
    echo "At least one condition is true";
}

// not / ! operator
if($a !== 1) {
    echo "Condition is true";
}


?>